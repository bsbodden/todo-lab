package dev.kmpilot.todo.gen

/**
 * GENERATOR PoC — NOT app runtime code.
 *
 * The platform-agnostic *entity descriptor* — KMPilot's "App + schema" moat in miniature: ONE ownership policy +
 * field model, emitted to MULTIPLE backend targets (Postgres migration + RLS, Firestore rules). The descriptor is the
 * intermediate the generator consumes; in the real product it is DERIVED from the DDD domain IR (here we hand-mirror
 * [dev.kmpilot.todo.domain.Task] in [taskDescriptor] and PROVE the emitted artifacts reproduce the committed, already
 * end-to-end-verified `supabase/migrations/…_create_tasks.sql` and `firestore.rules`).
 *
 * This lives in jvmMain (generator host) deliberately: it is build-time tooling, never shipped in the app binary.
 */

/** The cross-target field kinds the descriptor understands. Each maps to a SQL type and (implicitly) a Firestore value. */
enum class FieldType {
    /** Variable-length text → Postgres `text`. */
    STRING,
    /** 64-bit integer → Postgres `bigint`. */
    LONG,
    /** Boolean → Postgres `boolean`. */
    BOOL,
    /** Instant / date-time → Postgres `timestamptz`. */
    TIMESTAMP,
}

/**
 * One persisted field of an entity.
 *
 * @param name camelCase domain name (e.g. `ownerId`). The Postgres column is the snake_case of this; Firestore keeps
 *   the camelCase as-is (it stores the document field verbatim, which is why the rules reference `ownerId`, not `owner_id`).
 * @param type the cross-target kind (see [FieldType]).
 * @param nullable whether the column is NULL-able (a non-null field emits `not null`).
 * @param defaultBool a literal default for a non-null [BOOL] (the domain's `false` defaults for `isCompleted`/`isRepeating`).
 */
data class Field(
    val name: String,
    val type: FieldType,
    val nullable: Boolean = true,
    val defaultBool: Boolean? = null,
)

/**
 * A whole entity, as the generator sees it.
 *
 * @param name the domain type name (e.g. `Task`).
 * @param tableName the Postgres table (snake_case plural, e.g. `tasks`).
 * @param collection the Firestore collection (here == [tableName]).
 * @param ownerField the field carrying the owning user's id — the column/doc-field the ownership policy keys on
 *   (`ownerId`). This single value drives BOTH the Postgres RLS `owner_id = auth.uid()` and the Firestore
 *   `resource.data.ownerId == request.auth.uid` — the "one policy, many targets" thesis.
 * @param identityField the field that is the identity / surrogate primary key (`id` → `bigint generated by default as
 *   identity primary key`). It is server-assigned, so the adapter's insert omits it.
 * @param realtime when true, emit the Supabase realtime publication + `replica identity full` (Firestore is realtime
 *   by construction, so this flag only affects the Postgres target).
 * @param fields the persisted fields, IN COLUMN ORDER (excluding [identityField] and [ownerField], which the generator
 *   emits specially — id first, owner last).
 */
data class EntityDescriptor(
    val name: String,
    val tableName: String,
    val collection: String,
    val ownerField: String,
    val identityField: String,
    val realtime: Boolean,
    val fields: List<Field>,
)

/**
 * The `Task` entity as a descriptor — a hand-mirror of [dev.kmpilot.todo.domain.Task] (id, title, description,
 * categoryId, dueDate, creationDate, completedDate, isCompleted, isRepeating, alarmInterval, ownerId).
 *
 * Field order matches the committed migration's column order. `id` is the identity PK and `ownerId` is the owner field
 * (both handled specially by the generator, so neither appears in [EntityDescriptor.fields]). `alarmInterval` is an enum
 * in the domain; the schema persists it as text (its `name`), so it maps to [FieldType.STRING].
 */
val taskDescriptor: EntityDescriptor = EntityDescriptor(
    name = "Task",
    tableName = "tasks",
    collection = "tasks",
    ownerField = "ownerId",
    identityField = "id",
    realtime = true,
    fields = listOf(
        Field("title", FieldType.STRING, nullable = false),
        Field("description", FieldType.STRING, nullable = true),
        Field("categoryId", FieldType.LONG, nullable = true),
        Field("dueDate", FieldType.TIMESTAMP, nullable = true),
        Field("creationDate", FieldType.TIMESTAMP, nullable = true),
        Field("completedDate", FieldType.TIMESTAMP, nullable = true),
        Field("isCompleted", FieldType.BOOL, nullable = false, defaultBool = false),
        Field("isRepeating", FieldType.BOOL, nullable = false, defaultBool = false),
        Field("alarmInterval", FieldType.STRING, nullable = true),
    ),
)

/** camelCase → snake_case (e.g. `ownerId` → `owner_id`, `categoryId` → `category_id`). Pure, generator-internal. */
internal fun String.toSnakeCase(): String = buildString {
    for (ch in this@toSnakeCase) {
        if (ch.isUpperCase()) { append('_'); append(ch.lowercaseChar()) } else append(ch)
    }
}
