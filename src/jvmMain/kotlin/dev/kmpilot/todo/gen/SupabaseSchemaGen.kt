package dev.kmpilot.todo.gen

/**
 * GENERATOR PoC — emits the **Supabase Postgres migration** (table + RLS + grant + realtime) for an [EntityDescriptor].
 * One ownership policy (`ownerField`) → the full owner-private schema. Reproduces the committed, end-to-end-verified
 * `supabase/migrations/…_create_tasks.sql` (proven by [SchemaGenTest]); Postgres RLS auto-scopes rows server-side.
 */
object SupabaseSchemaGen {

    private fun sqlType(t: FieldType): String = when (t) {
        FieldType.STRING -> "text"
        FieldType.LONG -> "bigint"
        FieldType.BOOL -> "boolean"
        FieldType.TIMESTAMP -> "timestamptz"
    }

    fun migration(d: EntityDescriptor): String {
        val table = "public.${d.tableName}"
        val ownerCol = d.ownerField.toSnakeCase()

        val cols = buildList {
            // identity PK first
            add("${d.identityField.toSnakeCase()} bigint generated by default as identity primary key")
            // the domain fields, in order
            for (f in d.fields) {
                val nn = if (f.nullable) "" else " not null"
                val def = f.defaultBool?.let { " default $it" } ?: ""
                add("${f.name.toSnakeCase()} ${sqlType(f.type)}$nn$def")
            }
            // owner column last — FK to the auth user, defaulted to the caller
            add("$ownerCol uuid not null default auth.uid() references auth.users (id) on delete cascade")
        }

        fun policy(op: String, clause: String) =
            "create policy \"${d.tableName} are owner-private ($op)\" on $table for $op $clause;"
        val ownerIs = "$ownerCol = auth.uid()"

        return buildString {
            appendLine("-- GENERATED from the domain entity '${d.name}' (KMPilot App+schema). Ownership ($ownerCol) → RLS.")
            appendLine("create table $table (")
            append(cols.joinToString(",\n") { "    $it" }); appendLine()
            appendLine(");")
            appendLine()
            appendLine("alter table $table enable row level security;")
            appendLine()
            appendLine("grant select, insert, update, delete on $table to authenticated;")
            appendLine("grant usage, select on all sequences in schema public to authenticated;")
            appendLine()
            appendLine(policy("select", "using ($ownerIs)"))
            appendLine(policy("insert", "with check ($ownerIs)"))
            appendLine(policy("update", "using ($ownerIs) with check ($ownerIs)"))
            appendLine(policy("delete", "using ($ownerIs)"))
            if (d.realtime) {
                appendLine()
                appendLine("alter publication supabase_realtime add table $table;")
                appendLine("alter table $table replica identity full;")
            }
        }
    }
}
