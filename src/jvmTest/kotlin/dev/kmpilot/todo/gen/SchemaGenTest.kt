package dev.kmpilot.todo.gen

import java.io.File
import kotlin.test.Test
import kotlin.test.assertTrue
import kotlin.test.fail

/**
 * THE "App + schema" GENERATION PROOF — generating from the [taskDescriptor] reproduces the committed, already
 * end-to-end-verified `supabase/migrations/…_create_tasks.sql` and `firestore.rules`. ONE descriptor → both backends'
 * schema/policies, semantically equal to what we hand-wrote and proved live (SupabaseSwapTest / FirebaseSwapTest).
 * This is the moat: the domain is the single source of truth for the backend's schema + access rules.
 *
 * Assertion is SEMANTIC (whitespace/case-normalized substring presence), not byte-equality — formatting is irrelevant;
 * the DDL/policies/rules are what must match. Each fragment is asserted in BOTH the generated output AND the committed
 * file, so the generator is held to the proven artifact, not just to itself.
 */
class SchemaGenTest {

    private fun norm(s: String) = s.lowercase().replace(Regex("\\s+"), " ").trim()

    private fun committed(path: String): String {
        val f = File(path)
        if (!f.exists()) fail("expected committed artifact not found: ${f.absolutePath} (cwd=${File(".").absolutePath})")
        return f.readText()
    }

    private fun assertBothContain(generated: String, committedText: String, fragments: List<String>) {
        val g = norm(generated); val c = norm(committedText)
        for (frag in fragments) {
            val n = norm(frag)
            assertTrue(g.contains(n), "GENERATED is missing: $frag")
            assertTrue(c.contains(n), "COMMITTED is missing (drifted from the generator?): $frag")
        }
    }

    @Test
    fun supabase_migration_reproduces_the_committed_schema() {
        val generated = SupabaseSchemaGen.migration(taskDescriptor)
        val committedFile = File("supabase/migrations").listFiles { f -> f.name.endsWith("_create_tasks.sql") }
            ?.firstOrNull() ?: fail("no *_create_tasks.sql under supabase/migrations")
        assertBothContain(generated, committedFile.readText(), listOf(
            "create table public.tasks (",
            "id bigint generated by default as identity primary key",
            "title text not null",
            "description text",
            "category_id bigint",
            "due_date timestamptz",
            "creation_date timestamptz",
            "completed_date timestamptz",
            "is_completed boolean not null default false",
            "is_repeating boolean not null default false",
            "alarm_interval text",
            "owner_id uuid not null default auth.uid() references auth.users (id) on delete cascade",
            "alter table public.tasks enable row level security",
            "grant select, insert, update, delete on public.tasks to authenticated",
            "grant usage, select on all sequences in schema public to authenticated",
            "for select using (owner_id = auth.uid())",
            "for insert with check (owner_id = auth.uid())",
            "for update using (owner_id = auth.uid()) with check (owner_id = auth.uid())",
            "for delete using (owner_id = auth.uid())",
            "alter publication supabase_realtime add table public.tasks",
            "replica identity full",
        ))
    }

    @Test
    fun firestore_rules_reproduce_the_committed_rules() {
        val generated = FirestoreRulesGen.rules(taskDescriptor)
        assertBothContain(generated, committed("firestore.rules"), listOf(
            "rules_version = '2'",
            "match /tasks/{taskid}",
            "request.auth != null && resource != null && resource.data.ownerid == request.auth.uid",
            "request.auth != null && request.resource.data.ownerid == request.auth.uid",
        ))
    }
}
